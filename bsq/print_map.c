#include <unistd.h>

void	print_map(char	map[4096])
{
	int	i;

	i = 0;
	while (map[i] != '\n')
	{
		i++;
	}
	i++;
	while (map[i] != '\0')
	{
		write(1, &map[i], 1);
		i++;
	}
}
