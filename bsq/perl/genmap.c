void	genmap(char map[1000][1000])
{
	int	i;
	int	j;
	int	maxi;
	int	maxj;

	maxi = 9;
	maxj = 27;
	i = 0;
	while (i < maxi)
	{
		j = 0;
		while (j < maxj)
		{
			map[i][j] = '.';
			j++;
		}
		i++;
	}
	map[1][4] = 'o';
	map[2][12] = 'o';
	map[4][4] = 'o';
	map[5][15] = 'o';
	map[7][6] = 'o';
	map[7][21] = 'o';
	map[8][2] = 'o';
	map[8][10] = 'o';
}
